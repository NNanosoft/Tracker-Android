package com.adsamcik.tracker.shared.base.constant

import kotlin.math.PI

/**
 * Geometric constants.
 */
object GeometryConstants {
	/**
	 * Half a circle in degrees.
	 */
	const val HALF_CIRCLE_IN_DEGREES: Double = 180.0

	/**
	 * Full circle in degrees.
	 */
	const val CIRCLE_IN_DEGREES: Double = 2 * HALF_CIRCLE_IN_DEGREES

	/**
	 * Half a circle in radians.
	 */
	const val HALF_CIRCLE_IN_RADIANS: Double = PI

	/**
	 * Full circle in radians.
	 */
	const val CIRCLE_IN_RADIANS: Double = 2 * HALF_CIRCLE_IN_RADIANS
}
